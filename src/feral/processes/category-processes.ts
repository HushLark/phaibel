// ─────────────────────────────────────────────────────────────────────────────
// Category Processes — Pre-built Feral processes for classified request types
//
// These are data-gathering pipelines: they collect relevant context from CxMS
// and let feralChatHeadless's synthesizeResponse() turn results into a reply.
//
// Categories covered here (deterministic, efficient):
//   query         → search entities → synthesize
//   analytical    → list types + search → synthesize with analysis
//   introspection → list types + search → synthesize with capability overview
//
// Categories NOT covered (handled by the existing LLM-driven pipeline):
//   task, remember, create  — arbitrary entity operations; dynamic processes
//                             generated by the reason model are better suited
//   chat    — bypassed entirely in feralChatHeadless before process dispatch
//   none    — falls through to existing pipeline
// ─────────────────────────────────────────────────────────────────────────────

import { hydrateProcess } from '../process/process-json-hydrator.js';
import type { Process } from '../process/process.js';
import type { RequestCategory } from '../../context/request-classifier.js';

// ── query ────────────────────────────────────────────────────────────────────
// Searches CxMS for entities matching the user's question.
// search_entities auto-falls back to user_input when no query is set.

const queryProcess: Process = hydrateProcess({
    schema_version: 1,
    key: 'phaibel.query',
    description: 'Retrieve stored information to answer user questions about tasks, events, notes, goals, people, or any other tracked data',
    context: {},
    nodes: [
        {
            key: 'start',
            catalog_node_key: 'start',
            configuration: {},
            edges: { ok: 'search' },
        },
        {
            key: 'search',
            description: 'Search for entities matching the question',
            catalog_node_key: 'search_entities',
            configuration: { context_path: 'search_results' },
            edges: { ok: 'done', not_found: 'done', error: 'done' },
        },
        {
            key: 'done',
            catalog_node_key: 'stop',
            configuration: {},
            edges: {},
        },
    ],
});

// ── analytical ───────────────────────────────────────────────────────────────
