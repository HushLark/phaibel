// ── factual ───────────────────────────────────────────────────────────────────
// Runs a live web search for current real-world data: weather, exchange rates,
// news, flight status, sports scores, etc. Uses web_search node code which
// routes through Synaptic (subscription) or Perplexity direct API (BYOK).

const factualProcess: Process = hydrateProcess({
    schema_version: 1,
    key: 'phaibel.factual',
    description: 'Look up current real-world information: weather, exchange rates, stock prices, news, flight status, sports scores, or any live factual data',
    context: {},
    nodes: [
        {
            key: 'start',
            catalog_node_key: 'start',
            configuration: {},
            edges: { ok: 'search' },
        },
        {
            key: 'search',
            description: 'Search the web for current factual data',
            catalog_node_key: 'web_search',
            configuration: {
                query: '{user_input}',
                response_context_path: 'search_result',
            },
            edges: { ok: 'done', error: 'done' },
        },
        {
            key: 'done',
            catalog_node_key: 'stop',
            configuration: {},
            edges: {},
        },
    ],
});

